<h2><span class="mw-headline" id="Procedure">Procedure</span></h2>
<p><br />This procedure explains various ways to Apply for Certificate of Manufacture Organic Fertilizer or Bio Fertilizer in Chandigarh(Haryana).<br /><br />
<b>Apply In-Person</b><br />
</p>
<ol><li>Applicant in order to obtain a Certificate to Manufacture Organic Fertilizer/Bio Fertilizer should go to the “district agricultural office (DAO) in case of District &amp; SDAO in case of Sub-Division” in person and apply.</li>
<li>Applicant has to get the letter head for the firm and should get required document as per our page.</li>
<li>Link for contact details&#160;: <a target="_blank" rel="nofollow" class="external text" href="http://agriharyana.gov.in/index.php?r=site%2Fcontactus">Department of Agriculture</a> AND <a target="_blank" rel="nofollow" class="external text" href="http://ppqs.gov.in/regions/cipmcs">Department of plant production</a> please select the appropriate place for contact details.</li>
<li>Please collect the appropriate application form as per officer’s advice Or please download the for online using the following link <a target="_blank" rel="nofollow" class="external text" href="https://cfqcti.dacnet.nic.in/listfrm.html">Form link1</a> please refer FORM ‘D’ to apply </li>
<li>Submit the completed application (2 copies) along with required documents as mentioned in our required documents session.</li>
<li>After accepting the application, officials will advise the amount to be remitted as per the mode (treasury challan or as acceptable).</li>
<li>Applicant after remitting the mentioned amount should provide the proof of payment (receipt or challan) to the department authority for processing.</li>
<li>Applicant will get intimation for inspection of premises.</li>
<li>Concerned authorities will inspect the business place and do their due diligence for the machine / other apparatus as mentioned in application by applicant.</li>
<li>Once all the process yields satisfactory result, Applicant gets intimation to collect the certificate.</li>
<li>On the said date, applicant can collect the certificate.<br /><br /></li></ol>
<dl><dd>Note: 
<dl><dd><ul><li>Authorities will provide information on educational requirements as applicable to apply for the certificate. </li></ul></dd></dl></dd></dl>

<h2><span class="mw-headline" id="Required_Documents">Required Documents</span></span></h2>
<ul><li>GST details</li>
<li>Application form. <a target="_blank" rel="nofollow" class="external text" href="https://cfqcti.dacnet.nic.in/listfrm.html">Form link1</a> please refer FORM ‘D’</li>
<li>Attested copy of Memorandum of article and association/Partnership deed/Affidavit regarding Sole proprietor</li>
<li>List of present Directors in case of Limited/ Pvt. Limited Company (Name &amp; complete office &amp; Residential address)</li>
<li>Power of attorney / Resolution to a point responsible person U/C 24 of FCO 1985 to sign and submit the documents.</li>
<li>General affidavit by Competent person authorised by the resolution of the Firm /Partners / Proprietor himself along with photo and residential proof</li>
<li>Affidavit of responsible person under clause 24 of FCO 1985 nominated by resolution of the Firm / Partners / Proprietor himself along with photo and residential proof</li>
<li>Map of the factory and proof of the ownership / rent deed for that godown</li>
<li>List of Lab, equipments under clause 21 A of FCO 1985</li>
<li>Qualification proof of lab chemist</li>
<li>Registration Fee</li>
<li>Land documents in Details (Where the manufacturing takes place).</li>
<li>NOC from the Local body as appropriate</li>
<li>NOC from State Pollution Control Board.</li>
<li>List of Tech Expert with their Qualification,</li>
<li>First Aid facility (from the contact Doctor),</li>
<li>Product ingredient which are to be formulated.</li>
<li>Rubber stamp in the company name</li>
<li>Additional documents (If Any)</li>
<li>ID proof</li>
<li>Address proof</li>
<li>Photo</li>
<li>Aadhaar Card </li>
<li>An undertaking that applicant shall abide by the terms and conditions </li></ul>
