<h3>What is FSSAI/Food License</h3>
<p>Food License Registration certificate is required to run a food product business. You have to register under Food Safety and Standards Authority of India (FSSAI) to manufacture, distribute and transport food products. FSSAI is established under the Ministry of Health & Family Welfare, Government of India. The FSSAI has been established under the Food Safety and Standards Act, 2006. FSSAI Certificate and FSSAI License are same which ensures the quality, purity and other important factors the customers can rely upon.</p>
		 
		 <strong>REGISTRATION</strong></p>
<p style="text-align:justify">
	As per Section 31(1) & 31(2) of  <a href="https://fssai.gov.in/cms/food-safety-and-standards-act-2006.php" target="_blank">FSS Act, 2006</a> every Food Business Operator in the country is required to be licensed/registered under the Food Safety &Standards Authority of India. The licensing and registration procedure and requirements are regulated by <a  href='https://fssai.gov.in/upload/uploadfiles/files/Licensing_Regulations.pdf' target='_blank'  title='Food Safety & Standards (Licensing and Registration of food Business) Regulations, 2011'>Food Safety & Standards (Licensing and Registration of food Business) Regulations, 2011&nbsp;<img src='https://fssai.gov.in/images/pdf_icon.png' alt='Pdf'> size:( 0.17 MB)</a>. Registration is meant for petty food manufacturers that includes petty retailer, hawker, itinerant vendor or a temporary stall holder or small or cottage scale industry having <strong>annual turnover up to 12 lacs</strong>. All food businesses having income more than this limit are required to take a license.</p><br>
