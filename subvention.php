     <div class="CNT-bullet"> <span id="ContentPlaceHolder1_lbldescription"><div class="format"></div>
<div class="format">The Honourable Finance Minister in his budget speech (para 49) for 2006-07 announced that the Government had decided to ensure that farmers receive short term credit at 7% with an upper limit of Rs. 3.00 lakh on the principal amount. The policy came into force with effect from Kharif 2006-07. The amount of subvention was to be calculated on the amount of crop loan from the date of disbursement up to the actual date of repayment of the crop loan by the farmer or up to the due date of the loan fixed by the banks, whichever is earlier, subject to a maximum period of one year.</div><div>&nbsp;</div>

<div class="format">In pursuance of this announcement, the Government of India provided interest subvention of 2% to Public Sector Banks, Regional Rural Banks (RRBs) and Cooperative Banks in respect of short term production credit up to Rs. 3.00 lakh provided to farmers out of their own resources, provided that they make available short term credit @ 7% p.a. at ground level. Private Sector Banks (in respect of loans given by their rural and semi urban branches) are also covered under the scheme from the year 2013-14 with similar terms and conditions.</div>

<div class="format">The scheme, introduced in 2006-07, has continued in the following years with certain modifications and changes in rate of subvention. Year-wise rate of interest subvention made available to the banks on their own funds was as under:</div>
<div>&nbsp;</div>
<div class="format">2007-08 - 2%&nbsp;</div>
<div class="format">2008-09 - 3%</div>
<div class="format">2009-10 - 2%</div>
<div class="format">2010-11 - 1.5%</div>
<div class="format">2011-12 - 2%</div>
<div class="format">2012-13 - 2%</div>
<div class="format">2013-14 - 2%</div>
<div class="format">2014-15 - 2%&nbsp;</div>
<div class="format">2015-16 - 2%</div>
<div class="format">2016-17 - 2%</div>
<div class="format">2017-18- 2%</div>
<br>
<div class="format"><ul><li><strong><h3 style="color: #008000;">Incentive to farmers on prompt repayments&nbsp; &nbsp;</h3></strong></li></ul></div>
<div class="format"></div>
<div class="format">Since the year 2009-10, Government of India introduced additional incentive for farmers who promptly repay the loans on or before the due date or the date fixed by the bank, subject to a maximum period of one year. Year-wise rate of incentive to prompt-paying farmers was as under:</div>

<div class="format">2009-10 - 1%</div>
<div class="format">2010-11 - 2%</div>
<div class="format">2011-12 - 3%</div>
<div class="format">2012-13 - 3%&nbsp;</div>
<div class="format">2013-14 - 3%</div>
<div class="format">2014-15 - 3%</div>
<div class="format">2015-16 - 3%</div>
<div class="format">2016-17 - 3%</div>
<div class="format">2017-18- 3%</div>

<div class="format"><ul><li><strong><h3 style="color: #008000;">Relief to farmers</h3></strong></li></ul></div>
<div class="format"></div>
<div class="format">To provide relief to farmers affected by natural calamities, Interest Subvention of 2% has been made available to banks for the first year on restructured amount of crop loans.&nbsp; Such restructured loans will attract normal rate of interest from the second year onwards as per the policy laid down by the RBI.</div><br>
<div class="format"><ul><li><h3 style="color: #008000;">Interest Subvention to Small and Marginal Farmers against Negotiable Warehouse Receipts&nbsp;&nbsp;</h3></li></ul></div>

<div class="format">In order to discourage distress sale of produce by farmers and to encourage them to store their produce in warehouses against warehouse receipts, GoI had introduced a scheme in 2011-12 for extending concessional loans to the farmers against negotiable warehouse receipts.</div><div>&nbsp;</div>
<div class="format"></div>
<div class="format">The benefit of 2% interest subvention will be available to banks on their own funds involved for extending credit support up to Rs 3.00 lakh at 7% interest per annum to Small and Marginal farmers (SF/MF) having Kisan Credit Card for a further period of upto six months post the harvest of the crop against Negotiable Warehouse Receipts issued on the produce stored in warehouses accredited with Warehousing Development Regulatory Authority.</div>
<div class="format"></div>
<div class="format">SF/MF, who have not availed crop loans through banking system, would not be eligible.</div>
