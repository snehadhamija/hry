	<h3>NOTIFICATION <h3>
	<h4>The 6th October, 2015</h4></center>
	<p style="text-align:justify;"> No. 49/43/2015 – 4IB1.  In pursuance to the provision under Chapter 12 of Enterprises Promotion Policy-2015 notified vide No. 49/43/2015 - 4IB1 dated 14th August, 2015, the Governor of Haryana is  pleased  to  notify  the  “Restrictive  List  of  Industries”  of  the  State  as  under.  The  incentives under the policy will not be available to these industries except exclusive incentive of Freight Assistance,  State  Export  Awards  to  export  oriented  units  and  State  Awards  for  MSMEs.  The State would  not  encourage the  following  categories  of Industries  for reasons  of pollution or water scarcity or planning perspective:</p>

	<ol>
	
	<li>Industrial units involving trade effluents and air emissions setup within or operating from the residential areas;</li>
<li>Cigars and Cigarettes of Tobacco and manufacture of Tobacco including other Tobacco products;</li>
<li>Induction and ARC furnace with more than 0.6 MT capacity (restricted due to high energy consuming units);</li>
<li>Industrial units for soft drinks/ aerated water and packaged drinking water, (consuming high water content) located in areas notified as “Dark Zones” for water availability;</li>
<li>Stone crushers/ Washeries for reasons of Air and Water Pollution;</li>
<li>Lime kilns, Brick kilns except refractory bricks, fly Ash bricks and cement blocks;</li>
<li>Sugar Industry for reasons of pollution, smell and non-availability of raw material;</li>
<li>Copper smelter/ Zinc smelter recovery of Zinc metal from Zinc ash, Dross and Waster for reason of pollution;</li>
<li>Manufacture of Ethyl Alcohol/ Distillery/ Fermentation/ Brewery except wines/ liquors/ brandy etc. made from 100% fruits and Barley produced in the State;</li>
<li>Sulphuric acid/ thinners & varnishes/ electroplating for reason of pollution;</li>
<li>Dyes and Dye intermediates for reasons of high pollution except units covered under zero liquid discharge;</li>
<li>Dyeing Industry for yarn and cloth except units covered under zero liquid discharge;</li>
<li>Refining of used oil for reason of pollution;</li>
<li>Secondary processing of Iron and Steel manufacturing of CR sheets, Galvanized Sheets and Castings outside the designated zones;</li>
<li>Cold storage being service industry and not providing service directly to manufacturing;</li>
<li>Tanneries for reason of air and water pollution;</li>
<li>Poultry excluding Hatcheries;</li>
<li>Fire crackers Manufacturing for reason of explosive & hazardous industry;</li>
<li>Hot mix plants including site oriented industries for reason of pollution;</li>
<li>Incandescent Electric Bulbs and fluorescent bulbs/ tubes as an energy conservation measure;</li>
</ol>
<p><b>Note: </b>The above list may be reviewed by the Government from time to time.</p>
