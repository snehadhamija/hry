	<h2><center style="color:red">	Restrictive list:	</center></h2> <br>
<p><b>	The incentives under the policy will not be available to these industries except State Export Awards and State Awards for MSMEs. The State would not encourage the following categories of Industries for reasons of pollution or water scarcity or planning perspective:	</b></p><br><br>
<p>	1.     Industrial units involving trade effluents and air emissions setup within or operating from the residential areas;	</p>
<p>	2.     Cigars and Cigarettes of Tobacco and manufacture of Tobacco including other Tobacco products;	</p>
<p>	3.     Industrial units for soft drinks/ aerated water and packaged drinking water, (consuming high-water content) located in areas notified as “Dark Zones” for  water availability;	</p>
<p>	4.     Stone crushers/ Washeries for reasons of Air and Water Pollution;	</p>
<p>	5.     Lime kilns, Brick kilns except refractory bricks, fly Ash bricks and cement blocks;	</p>
<p>	6.     Copper smelter/ Zinc smelter recovery of Zinc metal from Zinc ash, Dross and Waster for reason of pollution;	</p>
<p>	7.     Manufacture of Ethyl Alcohol/ Distillery/ Fermentation/ Brewery except wines/ liquors/ brandy etc. made from 100% fruits and Barley produced in the State;	</p>
<p>	8.     Sulphuric acid/ thinners & varnishes/ electroplating for reason of pollution;	</p>
<p>	9.     Dyes and Dye intermediates for reasons of high pollution except units covered under zero liquid discharge;	</p>
<p>	10.  Dyeing Industry for yarn and cloth except units covered under zero liquid discharge;	</p>
<p>	11.  Refining of used oil for reason of pollution;	</p>
<p>	12.  Tanneries for reason of air and water pollution;	</p>
<p>	13.  Poultry excluding Hatcheries;	</p>
<p>	14.  Fire crackers Manufacturing for reason of explosive & hazardous industry;	</p>
<p>	15.  Hot mix plants including site-oriented industries for reason of pollution;	</p>

<p><b>&nbsp;&nbsp;Note: </b>The above list may be reviewed by the Government from time to time.</p>
