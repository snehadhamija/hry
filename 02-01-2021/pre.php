<b class="format">
           <center> <h2>Procurement & Marketing Support Scheme</h2></center>
        </b>
<p>
			Marketing, a strategic tool for business development, is critical for the growth and survival of MSMEs. Due to the lack of information, scarcity of resources and unorganized ways of selling / marketing, MSME sector often faces problems in exploring new markets and retaining the existing ones. Keeping in view these facts, the Procurement and Marketing support Scheme has been introduced to enhance the marketability of products and services in the MSME sector.</p>
			
	<p class="text-danger"><b>OBJECTIVE:</b></p>
	<p>	The scheme aims at the following:<br>
	<ol>
	<li>Promoting new market access initiatives like organising / participation in National /.International Trade Fairs / Exhibitions I MSME Expo etc. <br>
	</li><li>To create awareness and educate the MSMEs about importance / methods / process of packaging in marketing, latest packaging technology, import-export policy and procedure, GeM portal, MSME Conclave, latest developments in international / national trade and other subjects I topics relevant for market access developments. <br>
	</li><li>To create more awareness about trade fairs, bar code, digital advertising, e-marketing, GST, GeM portal, public procurement policy and other related topics etc.</ol>
    </p>
	
    <p  class="format"><b>SCHEME COMPONENTS FOR MARKETIBILITY ENHANCEMENT:</b></p>
	<p>
	<ol>
	<li>Participation of individual MSEs in domestic trade fairs / exhibitions across the country. </li>
	<li>Organizing/Participation in trade fairs/ exhibitions by the Ministry/ Office of DC (MSME)/ Government Organizations. </li>
	<li>	Vendor Development Program (VDP). </li>
	<li>	International/National Workshops/Seminars  </li>
	<li>	Awareness Programs. </li>  </ol> </p>


	<p  class="format"><b>ELIGIBILITY CONDITION:</b></p>
	<p>	 Manufacturing I Service sector MSEs registered at Udyog Aadhar Memorandum (UAM) Portal.
    </p>
	
	
	<p  class="format"><b>IMPLEMENTING AGENCY:</b></p>
	<p>	
	<ol>
	<li>DC MSME through its field organisations namely MSME Development institutes and Technology centres.</li>
	<li>Other field organisations of M/o MSME namely NSIC, KVIC, Coir board.</li>
	<li>State governments through its departments/organisations/corporations/autonomous bodies and agencies.</li>
	<li>Other Central Govt ministries through its departments/ organisations/ corporations/ autonomous bodies and agencies.</li></ol>

    </p>
	<p  class="format"><b>SELECTION OF MSEs:</b></p>
	<p>
	The unit will apply to the Office of DC (MSME)/ M/o MSME directly or through Implementing agency online (or system in place), well in advance (preferably one month) prior to the commencement of trade fairs/ exhibitions. After due scrutiny, the programme division will submit the proposal to the Empowered Committee for the final approval.
    </p>
	
