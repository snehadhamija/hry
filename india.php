<p>Make in India is a major national programme of the Government of India designed to facilitate investment, foster innovation, enhance skill development, protect intellectual property and build best in class manufacturing infrastructure in the country. The primary objective of this initiative is to attract investments from across the globe and strengthen India’s manufacturing sector. It is being led by the Department for Promotion of Industry and Internal Trade (DPIIT), Ministry of Commerce and Industry, Government of India. The Make in India programme is very important for the economic growth of India as it aims at utilising the existing Indian talent base, creating additional employment opportunities and empowering secondary and tertiary sector. The programme also aims at improving India’s rank on the Ease of Doing Business index by eliminating the unnecessary laws and regulations, making bureaucratic processes easier, making the government more transparent, responsive and accountable.</p>
<p>
“I want to tell the people of the whole world: Come, make in India. Come and manufacture in India. Go and sell in any country of the world, but manufacture here. We have skill, talent, discipline and the desire to do something. We want to give the world an opportunity that come make in India,” Prime Minister of India, Mr Narendra Modi said while introducing the programme in his maiden Independence Day speech from the ramparts of the Red Fort on August 15, 2014. The initiative was formally introduced on September 25, 2014 by Mr Modi at Vigyan Bhawan, New Delhi, in the presence of business giants from India.</p>
<p>
The focus of Make in India programme is on 25 sectors. These include: automobiles, automobile components, aviation, biotechnology, chemicals, construction, defence manufacturing electrical machinery, electronic systems, food processing, IT & BPM, leather, media and entertainment, mining, oil and gas, pharmaceuticals, ports and shipping, railways, renewable energy, roads and highways, space, textile and garments, thermal power, tourism and hospitality and wellness.</p>
<p>
The dedicated website for this initiative (www.makeinindia.com) not only showcases the 25 sectors but also puts focus on opportunities, policies and Ease of Doing Business. The Investor Desk is an integral part of this website, which aims at providing all information/data analysis to investors across sectors.</p>

