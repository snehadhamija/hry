                                <h4>III. Application for grant of Licence:-</h4>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The colonizers has been defined 
						in the Act i.e. an individual , company or association, body of individuals 
						whether incorporated or not, owing land for converting it into a colony and to 
						whom a licence has been granted under this Act. In accordance with the above 
						definition following kind of applications are being considered:-
                                </p>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
                                        <p>
                                            <b>1.</b> The ‘company’ having ownership of land in accordance with the above 
								parameters.
								<br>
                                            <b>2.</b> Individual having ownership of land in accordance with the above 
								parameters.
								<br>
                                            <b>3.</b> Group of persons i.e. body of individual may also pool together their 
								land falling in the compact block in accordance with above minimum area 
								parameter and may apply for grant of licence after executing a collaboration 
								agreement with a reputed builder having sound financial capability to develop 
								the colony.
                                        </p>
                                    </div>
                                </div>
                                <h4>IV. Conformity to the Land Use Plan:-</h4>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The land applied for licence 
						should be in conformity with the land use plan and the permissible uses as 
						defined in the zoning regulations of the Development Plan published/approved 
						for the controlled area under the Punjab Scheduled Roads and Controlled Areas 
						Restriction of Unregulated Development Act, 963 or the Punjab New (Capital) 
						Periphery Control Act, 1952(for periphery area of Panchkula only).
                                </p>
                                <h4>V. Release of Land from Acquisition:-</h4>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
                                        <p>
                                            <b>1.</b> The land should be free from all encumbrances including the 
								proceedings under the Land Acquisition Act, 1894. In case the ownership of the 
								land is prior to notification of under section 4 of the Land Acquisition Act, 
								the application can be considered for grant of licence on merits of each case 
								provided Government grants concurrence and decides to release the land.
								<br>
                                            <b>2.</b> The colonizers should have sound capacity to develop the colony and 
								needs to submit proof of his financial capacity along with the application.
                                        </p>
                                    </div>
                                </div>
                                <h4>VI. Parameters for Grant of Licence:-</h4>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The different parameters for each 
						category of colony are detailed below:-
                                </p>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
                                        <h4>Residential Plotted Colony:-</h4>
                                        <div align="right" style="WIDTH: 100%">
                                            <div align="justify" style="WIDTH: 95%" class="T1">
                                                <b>a)</b> 	The plotable area/saleable area in a plotted colony cannot exceed more than 55% of the area of the colony (inclusive of 4% commercial area for need of the residents of the colony) and remaining area is to be utilized for planning of roads, community buildings like schools, hospitals , utility buildings/sites and open spaces.
							<br>
                                                <b>b)</b> 	The colonizer is required to provide for community building sites in accordance with the norms approved for the purpose. These norms are population based and are arrived at by taking into account the designated densities as envisaged in the Development Plan proposals. The minimum width of the road is 12 mtrs.
							<br>
                                                <b>c)</b> 	20% of the plots are to be reserved for EWS , housing with a minimum plot size of 50 sq. mtrs.
							<br>
                                                <b>d)</b>	25% of the total plots are to be allotted under the category of ‘No Profits No Loss’ plots ( NPNL) i.e. at the rate prescribed by the Director. The size of these plots ranges between 125 sq. mtrs to 225 sq. mtrs.
							<br>
                                                <b>e)</b>	The population to be achieved in the colony cannot exceed beyond the designated densities in the Development Plan.
                                            </div>
                                        </div>
                                        <h4>Group Housing:-</h4>
                                        <div align="right" style="WIDTH: 100%">
                                            <div align="justify" style="WIDTH: 95%" class="T1">
                                                <b>a)</b> 	The Group Housing site is governed in accordance with the zoning regulations approved by the Director.
							<br>
                                                <b>b)</b> 	The ground coverage in the group housing project as 35% of the site area and the floor area ratio (FAR) is 175.
							<br>
                                                <b>c)</b> 	The maximum habitable height in a group housing complex is 60 mtrs .
							<br>
                                                <b>d)</b>	The group housing project should not exceed 20% of the sector area.
							<br>
                                                <b>e)</b>	The community facilities are to be provided in accordance with the norms approved by the Department and are based on the population to be achieved as per proposed density of the group housing complexes which ranges from 100 to 400 persons per acre. 
							<br>
                                                <b>f)</b>	To provide convenient shopping within the group housing complex 0.5% of the site area can be utilized towards convenient shopping , these shopping is single storey with a maximum height of 4 mtrs.
							<br>
                                                <b>g)</b>	15% of the total number of flats are reserved for EWS and 10% of the main dwelling units is required for service apartments i.e. for domestic help. 
							<br>
                                                <b>h)</b>	The minimum two level basement for parking and services with a compulsory provision of one car space for every flat of the group housing complexes (except EWS).
							<br>
                                                <b>i)</b>   Area requirement for EWS and  service apartment is 200 sq.ft. and 140 sq.ft. respectively.
							<br>
                                                <b>j)</b>	It is also mandatory to provide 15% organized green space in a group housing complex.
                                            </div>
                                        </div>
                                        <h4>Commercial:-</h4>
                                        <p class="T1">
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The zoning regulations of development plans have been amended to allow private sector to undertake development of maximum 50% of the designated commercial areas in the development plans by way of licences.  The parameters for licence in residential colonies are as below:-
                                        </p>
                                        <div align="right" style="WIDTH: 100%">
                                            <div align="justify" style="WIDTH: 95%" class="T1">
                                                <b>a)</b> 	The commercial colonies are also granted licences for sites falling  along the peripheral/sector road in the designated residential sector of the Development Plan.
							<br>
                                                <b>b)</b> 	The area under licenced colony in a residential sector cannot exceed 3.5% of the sector area subject to minimum and maximum prescribed area limits.
							<br>
                                                <b>c)</b> 	The location of the site should be along a sector peripheral road within a provision of service road.
							<br>
                                                <b>d)</b>	The ground coverage allowed in commercial project is 40% and the floor area ration is 150 or 175 as opted by a colonizer.
							<br>
                                                <b>e)</b>	Maximum Height of the habitable area is 60 mtrs.
							<br>
                                                <b>f)</b>	Three level basement is permitted for providing parking and services . No storage is allowed in the basement.
							<br>
                                                <b>g)</b>	The parking norms are one car space for every 75 sq. mtrs covered area.
                                            </div>
                                        </div>
                                        <h4>Cyber City/Cyber Park:-</h4>
                                        <div align="right" style="WIDTH: 100%">
                                            <div align="justify" style="WIDTH: 95%" class="T1">
                                                <b>a)</b> 	The location of the site should be on a sector peripheral road in residential or industrial sector in the Development Plan. In case of residential sectors the permissible area for IT Park/ Cyber Park will not exceed 5% of the sector area.
							<br>
                                                <b>b)</b> 	The permissible ground coverage is 40% and FAR is 250.
							<br>
                                                <b>c)</b> 	In case of cyber cities 10% of the area of the site can be utilized for Group Housing and 4% of the area for commercial use.
							<br>
                                                <b>d)</b>	In case of Cyber Parks only 4% of the area can be utilized towards commercial uses. No group housing is permissible.
							<br>
                                                <b>e)</b>	The parking requirement is one car space for every 40 sq. mtrs area achieved.
							<br>
                                                <b>f)</b>	Three level basement is permissible for parking and service. No storage is to be allowed.
							<br>
                                                <b>g)</b>	In the area designated for IT activities, no other use is permissible. In case of violation , the colonizer will be required to pay the fee and charges  on commercial rates for the area where violation are being committed. 
							<br>
                                                <b>h)</b>	In case of Cyber City, the colonizer is required to complete at least 30% of the IT area within 5 years of the grant of licence and in case of Cyber Park within three years of grant of licence.
                                            </div>
                                        </div>
