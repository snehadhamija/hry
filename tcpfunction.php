                                <h4>VIII. Grant of Change of Land Use Permission</h4>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The department grants change of land use permission for residential/ industrial/commercial/institutional/farm house/recreational use under the Punjab Scheduled Roads and Controlled Areas Restriction of Unregulated Development Act, 1963 and its Rules, 1965.
                                </p>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The applicant desiring to seek permission for change of land use for residential/ industrial/ commercial/ institutional/ farm house/ recreational use is required to submit his application on prescribed form CLU-I to Director, as provide under rule 26-A along with following:-
                                </p>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
                                        <p>
                                            <b>1.</b>  Scrutiny fee @ Rs. 10/- per sq. meters for uses other than industrial/recreational projects and Rs.2/- per sq. meters for industrial & recreational use.
						<br>
                                            <b>2.</b>	Copy of deeds showing the title of the applicant
						<br>
                                            <b>3.</b>	A survey plan of the land on a scale of 1 to 40 feet showing the existing means of access to the said land for the nearest public road and building and their nature falling within 100 yards of the said land.
						<br>
                                            <b>4.</b>	Shajra Plan.
						<br>
                                            <b>5.</b>	Land Utilization Plan.
						<br>
                                            <b>6.</b>	Potability of water certificate from recognized water-testing laboratory (for farmhouse).
						<br>
                                            <b>7.</b>	Project report
						<br>
                                            <b>8.</b>	SSI certificate/registration certificate with Industries Deptt (for Industries)
						<br>
                                            <b>9.</b>	Deputy Commissioner’s recommendation certificate (for petrol pump and institutional applications)
                                        </p>
                                    </div>
                                </div>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;As provided under rule 26-B, no application under rule-26-A shall be considered to be valid until a plan and a copy of deed required by rule 26-A has been furnished to the satisfaction of the Director. In case of failure of such compliance, the application together with the plan and copy of deed shall be return to the applicant for resubmission in accordance with these rules.
                                </p>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;As provided under rule 26-C (1) if, after scrutiny of the plan and other necessary enquiry, which the Director may deem fit, he is satisfied that the application is fit for the grant of permission, he shall before granting permission, call upon the applicant to full fill the conditions laid down in rule-D within a period of thirty days from the date of notice given to him under a registered cover. 
                                </p>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;If the applicant fails to fulfill these conditions within the period specified in sub-rule (1), the permission shall be refused. 
                                </p>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;As provided under rule-26-E (1), if the applicant has fulfilled all the conditions laid down in rule 26-D to the satisfaction of the Director, the Director shall grant the permission.
                                </p>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;As provided under rule 26-F, the permission granted under rule-26-E shall remain valid for a period of two years from the date of order during which period all works for putting the said land to the permitted use shall be completed; provided that the permission may be renewed up to a further period of one year if the Director is satisfied that the delay in execution of works was for reasons beyond the control of the applicant.
                                </p>
                                <h4>Flow of application: -</h4>
                                <p class="T1">
                                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The applicant submits the CLU application form in the field office alongwith the required scrutiny fee (Rs 10 per sq. mtr for uses other than recreational /industrial use & Rs 2 per sq. mtr for industrial /recreational use).  After examination of the case, field office forwards the CLU application alongwith report to circle office, which ultimately sent to the Directorate.  
                                </p>
                                <h4>Policies: -</h4>
                                <p class="T1">
                                    1.	Change of Land Use applications are considered/approved as per the provisions of zoning regulations.  However, Government can relax the zoning regulations as per the provisions of Section 7(A) of Punjab Scheduled Roads and Controlled Areas Restriction of Unregulated Development Act, 1963, which is reproduced below: -
                                </p>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
                                        <p>
                                            <i>[7A.  Power of relaxation. – The Government may, in public interest, relax any restrictions or conditions in so far as they relate to land use prescribed in the controlled area in exceptional circumstances.]
                                            </i>
                                            <br>
                                        </p>
                                    </div>
                                </div>
                                <p class="T1">
                                    2.	The change of land use application are considered in conformity with the zoning regulations and disposed off at the level of Director, Town & Country Planning, where the development plans are published.  However, where the development plans are not published decision on the CLU application is taken at the level of the Government.  It is pertinent to mention here that the grant of change of land use permission of petrol-pumps are referred to the Government for approval.
                                </p>
                                <p class="T1">
                                    3.	Government has taken a decision for not allowing the industrial units in the agriculture zone of Hyper/High Potential Zone.  However, the SSI-units upto 2 acres are allowed in agriculture zone of High Potential Zone falling in Industrially Backward Block.
                                </p>
                                <p class="T1">
                                    4.	In Medium Potential Zone industries are allowed on roads other than Scheduled Road and National Highway, which are non polluting/RIS/SSI upto 2 acres.  The NOC from Pollution Control Board is required for the industries to be located in Agriculture Zone.  The industries registration certificate is also required.  Minimum 30 feet wide rasta is required for locating the industries in Agriculture Zone.
                                </p>
                                <p class="T1">
                                    5.	The Government has approved a policy for Mega tourism and recreational projects in the controlled areas with the following parameters: -
                                </p>
                                <div align="right" style="WIDTH: 100%">
