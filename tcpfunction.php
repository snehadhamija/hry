                                <p>
                                    The Department of Town and 
						Country Planning, Haryana is responsible to regulate the development and also 
						to check the haphazard development in and around towns in accordance with the 
						provisions of following statutes:-
                                </p>
                                <div align="right" style="WIDTH: 100%">
                                    <div align="justify" style="WIDTH: 95%" class="T1">
