<h1><i class="fa fa-angle-double-right"></i>Motor Transport Workers ACT, 1961</h1>
<p>The Act aims to provide for the welfare of the Motor Transport Workers and to regulate the conditions of their work. The employer of a Motor Transport Undertaking is required to get the undertaking registered under the provisions of the Act. </p>
							
							
								<h3>Registering Authority</h3>
								<p>The Labour Commissioner is the Registering Authority under the Act for the State.</p>	
								
								<h3>Penalty</h3>
								<p>The penalty provided for contravention of any of the provisions of the Act or Rules made there under is imprisonment up to a period of three months or fine up to Rs.500/- or with both. In case of continuing contravention, an additional fine which may extend to Rs. 75/- for each day after the first conviction is also provided.</p>