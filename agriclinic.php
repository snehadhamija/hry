   <div class="bod-title"><span id="ContentPlaceHolder1_lblname"><h1 style="color: #008000;">Agriclinic and Agribusiness Centres Scheme  </h1> </span> </div>
     <div class="CNT-bullet"> <span id="ContentPlaceHolder1_lbldescription"><div>
<div class="format"">
<p class="format">The scheme aims to promote the establishment of Agri-Clinics and Agri-Business Centres (ACABC) all over the country.&nbsp;</p>
<h3><strong style="color: #008000;">Agri-Clinics</strong></h3>

<p class="format">Agri-Clinics are envisaged to provide expert advice and services to farmers on various aspects to enhance productivity of crops/animals and increase the incomes of farmers. Agri-Clinics provide support in the following areas:</p>
<div>
<ul class="abc">
     <li class="format">Soil health</li>
     <li class="format">Cropping Practices</li>
     <li class="format">Plant Protection</li>
     <li class="format">Crop Insurance</li>
     <li class="format">Post-Harvest Technology</li>
     <li class="format">Clinical services for Animals, Feed and Fodder Management</li>
     <li class="format">Prices of various crops in the market, etc.&nbsp;</li>
</ul>
</div>
<H3><strong style="color: #008000;">Agri-Business Centres</strong></H3>

<div>Agri-Business Centres are commercial units of agri-ventures established by trained agriculture professionals. Such ventures may include maintenance and custom hiring of farm equipment, sale of inputs and other services in agriculture and allied areas, including post-harvest management and market linkages for income generation and entrepreneurship development.&nbsp;</div>
<div></div>
<div>The scheme covers full financial support for training and handholding, provision of loan and credit-linked back ended composite subsidy.&nbsp;</div>
<div><strong style="color: #008000;">&nbsp;</strong></div>
<h3><strong style="color: #008000;">Objectives of the scheme</strong></h3>

<div>
<ul class="abc">
     <li class="format">To supplement efforts of public extension by necessarily providing extension and other services to the farmers on payment basis or free of cost as per the business model of agripreneur, local needs and affordability of target group of farmers</li>
     <li class="format">To support agricultural development</li>
     <li class="format">To create gainful self-employment opportunities for unemployed agricultural graduates, agricultural diploma holders, intermediate in agriculture and biological science graduates with Post Graduation in agri-related courses.&nbsp;&nbsp;</li>
</ul>
</div>
<br>
<p class="format"><strong style="color: #008000;">Who can benefit from the scheme?&nbsp;</strong></p>

<p class="format">The scheme is open to the following categories of candidates</p>


<div>
<ul class="abc">
     <li class="format">Graduates in agriculture and allied subjects from State Agriculture Universities (SAUs)/Central Agricultural Universities/Universities recognized by ICAR/UGC. Degree in Agriculture and allied subjects offered by other agencies are also considered subject to approval of Department of Agriculture &amp; Cooperation, GoI, on recommendation of the State Government</li>
     <li class="format">Diploma (with at least 50% marks)/Post Graduate Diploma holders in Agriculture and allied subjects from State Agricultural Universities, State Agriculture and Allied Departments and State Department of Technical Education.</li>
     <li class="format">Diploma in Agriculture and allied subjects offered by other agencies are also considered subject to approval of Department of Agriculture, Cooperation &amp; Farmers&#8217; Welfare, GoI on recommendation of the State Government</li>
     <li class="format">Biological Science Graduates with Post Graduation in Agriculture &amp; allied subjects</li>
     <li class="format">Degree courses recognised by UGC having more than 60 percent of the course content in Agriculture and allied subjects</li>
     <li class="format">Diploma/Post Graduate Diploma courses with more than 60 percent of course content in Agriculture and allied subjects, after B.Sc. with Biological Sciences, from recognised colleges and universities.</li>
     <li class="format">Agriculture related courses at Intermediate (i.e. plus two) level, with at least 55% marks.</li>
</ul>
</div>
