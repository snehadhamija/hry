<B><FONT FACE="Arial">
<P>MSME-MDA COMMITTEE</P>
</B><P></P>
<p align=justify><b><A NAME="market">SECTOR-SPECIFIC MARKET STUDIES</A></b><br><br>
                      Financial assistance for the conduct of market studies would 
                      be available for industry associations/EPCs/FIEO. These 
                      should be sector specific and in respect of those sectors 
                      where MSMEs have a significant presence. This will include 
                      studies commissioned by industry associations to assess 
                      impact of WTO agreements on a specific sector. The Office 
                      of DC(MSME) may also commission such studies independently 
                      under this scheme. Assistance for commissioned studies would 
                      be limited to an overall ceiling of Rs.2 lakhs per study.</p>

<p align=justify><b><a name="anti">ANTI-DUMPING CASES</a></b><br><br>
                      With the opening up of the economy, the use of unfair trade 
                      practices has come into sharper focus. Establishing the 
                      use of unfair trade practices such as dumping requires much 
                      groundwork. Assistance for initiating/contesting anti-dumping 
                      cases will therefore be made available to MSME associations. 
                      Such assistance would be limited to 50% of total costs involved 
                      or Rs. 1 lakh whichever is lower.<br>
                      <br>
                      Proposals in respect of market assistance/sector studies 
                      and for anti-dumping cases must be self-contained and should 
                      include details of activities proposed to be undertaken, 
                      breakup of costs in respect of each activity, bio-data of 
                      professionals proposed to be engaged and duration of time 
                      required for completion.<br>
                      <br>


<b>BAR CODE</b><br><br>

75% reimbursement to MSME unit for adoption of Bar Code.<br><br>


<b><a name="implement">IMPLEMENTATION OF THE SCHEME</a></b><br><br>

The scheme shall be implemented by the Export Promotion and Marketing Division of the Office of DC(MSME). Application forms are enclosed with these guidelines and are also available at MSME-DO's website at <a href=http://www.dcmsme.gov.in>www.dcmsme.gov.in</a>. At the start of each financial year, the Office of DC(MSME) shall hold a joint meeting with NSIC, SIDBI and the Ministry of Commerce to ensure that no duplication of funding or overlap occurs in the implementation of the scheme. The MSME MDA Committee shall also identify thrust commodities and thrust regions to be focussed on during the year.<br><br>
<B><FONT FACE="Arial">
<P><A NAME="ssimda">MSME-MDA COMMITTEE</A></P>
</B><P></P>

                  <div align="justify">Applications received in respect of each fair/event will 
                    be placed before the MSME - MDA Committee which shall meet 
                    at least a week to ten days before the start of the fair/event. 
                    The Committee shall be headed by the Development Commissioner 
                    (MSME) and shall include as its other members a representative 
                    from FIEO, a representative from the IF Wing attached to the 
                    Ministry of MSME&ARI, Director (Board & Policy) JDC (WTO) 
                    and JDC (EP&M)in the Office of the DC(MSME) and the Director 
                    (WTO) in the Office of the DC(MSME). The Director (EP&M) in 
                    the Office of the DC(MSME) shall be the Member Secretary.</p> 
