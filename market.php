<B><FONT FACE="Arial" SIZE=2><P><A NAME="exist">EXISTING SCHEME</A></P>
</B><FONT FACE="Arial" SIZE=2><P></P>
                  <P align=justify>&#9;A Market Development Assistance Scheme 
                    is currently operated by the Ministry of Commerce with a view 
                    to encourage exporters (including MSME exporters) to access 
                    and develop overseas markets. The scheme offers funding for 
                    participation in international fairs, study tours abroad, 
                    trade delegations, publicity, etc. Direct assistance under 
                    MDA for small- scale units is given for individual sales-cum-study 
                    tours, participation in fairs/exhibitions and publicity. SIDBI 
                    operates a scheme of direct assistance for financing activities 
                    relating to marketing of MSME products.</P>

                  <P align=justify>&#9;The Office of DC(MSME) has an existing 
                    scheme for participation in international fairs, whereby MSME 
                    entrepreneurs are encouraged to display their products at 
                    international exhibitions abroad. MSME-DO provides exhibition 
                    space and shipment of exhibits ex-Mumbai free of cost for 
                    this purpose.</P>

<B><FONT FACE="Arial" SIZE=2><P><A NAME="propose">PROPOSED SCHEME</A></P>
</B><FONT FACE="Arial" SIZE=2><P></P>

                  <P align=justify>&#9;As part of the comprehensive policy package 
                    for promotion and development of MSMEs announced on 30<sup>th</sup> 
                    August 2000, it was decided that the Small Industries Development 
                    Organisation should have a Market Development Assistance (MDA) 
                    scheme similar to the one obtaining in the Ministry of Commerce. 
                    It should be a Plan Scheme. </P>

                  <P align=justify>&#9;In pursuance of the above announcement 
                    and in recognition of the fact that (i) MSME exporters need 
                    to be encouraged in their efforts at tapping and developing 
                    overseas markets; and (ii) MSME-DO's participation in international 
                    fairs, if coupled with the presence of actual exporters, would 
                    lead to substantial increase in export business, it has been 
                    decided to operate a new scheme called <b>MSME Exporters Market 
                    Development Assistance (MSME-MDA) Scheme </B>. </P>

                  <P align=justify>The scheme to be operated in addition to the 
                    existing MSME-DO scheme for participation in international fairs 
                    will cover all activities for which direct assistance is given 
                    under the existing MDA scheme of the Ministry of Commerce. 
                    In addition, the scheme would also provide for financial assistance 
                    for commissioning market study reports and for initiating/contesting 
                    anti-dumping cases. The scheme came into operation w.e.f. 
                    30-08-2001.</P>

<B><FONT FACE="Arial">
                  <P><A NAME="indi">Market Development Assistance Scheme for Micro/ Small manufacturing
enterprises/ Small & Micro exporters (MSME-MDA)</A></P>
</B><P></P>

                  <P align=justify> The scheme offers funding for: <br>
                    1. Participation by manufacturing Small & Micro Enterprises 
                    in International Trade Fairs/ Exhibitions under MSME India 
                    stall.<br>
                    2. Sector specific market studies by Industry Associations/ 
                    Export Promotion Councils/ Federation of Indian Export Organisation.<br>
                    3. Initiating/ contesting anti-dumping cases by MSME Associations 
                    and<br>
                    4. Reimbursement of 75% of one time registration fee (w.e.f. 
                    Ist January 2002) and 75% of annual fees (recurring) (w.e.f. 
                    Ist June 2007) paid to GSI (Formerly EAN India) by Small & 
                    Micro units for the first three years for bar code. 
