					     <h3>Entrepreneurial and Managerial Development of SMEs through Incubators</h3>
        <h5>Ministry of Micro, Small and Medium Enterprises</h5>
        <p><p>There are multiple objectives of the scheme, some of which are:</p>
<ul class="abc">
<li>To promote emerging technological and knowledge based innovative ventures that seek the nurturing of ideas from professionals.</li>
<li>To promote and support untapped creativity of individual innovators and also to assist individual innovators to become technology based entrepreneurs.</li>
<li>To promote networking and forging of linkages with other constituents of the innovation chain for commercialization of their developments.</li>
</ul>
</p>

                                <h3>Scheme Benefits &amp; Highlight</h3>
<ul class="abc">
<li>Funding support for setting up of ‘Business Incubators (BI)’: The cost may vary from Rs 4 to 8 lakh for each incubatee/idea, subject to overall ceiling of Rs 62.5 lakh for each BI.<br />
<br />
a) Upgradation of infrastructure Rs 2.50 lakh<br />
<br />
b) Orientation/training Rs 1.28 lakh<br />
<br />
c) Administrative expenses Rs 0.22 lakh<br />
<br />
Total assistance per BI Rs 66.50 lakh</li>
</ul>
<p>Around 10 ideas will be incuabted by the BI</p>

    
<h3> Eligibility</h3> 
                                
                            
<ul class="abc">
<li>Any individual or MSME with innovative ideas ready for commercialisation can apply to the host institution (e.g., IITs, NITs, technical colleges, research institutes, etc.) in order to obtain fund support. See the list of host institutions at following web address: <a href="http://www.dcmsme.gov.in/schemes/Institutions_Detail.pdf" target="_blank">http://www.dcmsme.gov.in/schemes/Institutions_Detail.pdf</a><br />
<br />
Any technical institution (as given in the EoI) that wants to become a host institution can apply to the office of the Development Commissioner-MSME or their nearest MSME-DI for funding support.</li>
</ul>
<h3> 
                                Registration Process
                                
                           </h3> 

<p>Application can be made by the technical institution desirous of becoming the host institution, once a Request for Proposal (RFP)/ Expression of Interest (EoI) is released.<br />
<br />
Any individual or MSME can apply directly to their nearest host institution. A list of host institutions is given on the website: <a href="http://www.dcmsme.gov.in/schemes/Institutions_Detail.pdf" target="_blank">http://www.dcmsme.gov.in/schemes/Institutions_Detail.pdf</a></p>




