<p ><span style="color: #ff0000;">TRADEMARK REGISTRATION</span></p> <p style="text-align: center;" align="left"><span style="color: #ff0000;"><br /></span></p> <p align="left">A trademark is a visual representation attached to goods for the purpose of indicating their trade origin. Trademark as a mark capable of distinguishing the goods or services of one person from those of others.</p> <p align="left">&nbsp;<strong>A trademark serves the purpose of identifying the source of origin of goods and services. Trade mark perform the following functions:</strong></p> <ul> <li>It identifies the product and its origin. For example the trademark &lsquo;Brooke Bond&rsquo; identifies tea originating from the company manufacturing tea and marketing it under the mark</li> <li>It guarantees its quality. The quality of tea sold in the packs marked&nbsp; &lsquo;Brooke Bond&rsquo; tea would be similar but different from tea labeled with mark &lsquo;Taj Mahal&rsquo;</li> <li>It advertises the product</li> <li>It creates an image of the product in the mind of the public particularly consumers or the prospective consumers of the goods/services&nbsp;</li> </ul> <p align="left"><strong>To protect the registered trademark, the following remedies can be resorted:</strong></p> <ul> <li><strong>Civil Remedies</strong>&nbsp;<strong>:</strong>&nbsp;When instance of infringement and passing off occur, the court of competent jurisdiction can be moved for grant of interlocutory injunction, damages and account of profits</li> <li><strong>Criminal proceedings&nbsp;:</strong>&nbsp;Complaints may be made against the person causing infringement</li> </ul> <p><strong>Administrative Remedies</strong>- Opposing the registration of a deceptively similar trademark when the Trade Mark Registry is in the process of considering the grant of a trademark, can protect the trademark. The Registrar can also be moved for removal of a deceptively similar trademark, If registered.</p> <p>&nbsp;</p> 

