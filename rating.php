   <h3>Performance &amp; Credit Rating Scheme</h3>
        <h5>Ministry of Micro, Small &amp; Medium Enterprises</h5>
        <p><p>This scheme seeks to establish independent, trusted third party opinion on capabilities and credit-worthiness of MSEs, and makes credit available at attractive interest rates and will ensure better productivity. Under this scheme (as per the turnover of the MSE) a percent of Rating Agency charges are reimbursed by Ministry of SSI.</p>
