</ul>
<p>Turnover of MSE Re-imbursement of fee through NSIC:</p>
<ul class="abc">
<li>1. Up to Rs. 50 lakh - 75% of the fee or INR25,000 (whichever is less)</li>
<li>2. Above Rs. 50 to 200 lakh - 75% of the fee or INR30,000 (whichever is less)</li>
<li>3. More than Rs. 200 lakh - 75% of the fee or INR40,000 (whichever is less)</li>
</ul>
