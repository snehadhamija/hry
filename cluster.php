      <div class="field-content" ><h2 class="title" ><span>MSE-CDP</span></h2>
<hr /><h3 class="sub-title">Micro & Small Enterprises - Cluster Development Programme (MSE-CDP)</h3>
<div class="main-content">
                        <p style="text-align:left;padding:5px;">The Ministry of Micro, Small and Medium Enterprises (MSME), Government of India (GoI)
has adopted the Cluster Development approach as a key strategy for enhancing the
productivity and competitiveness as well as capacity building of Micro and Small
Enterprises (MSEs) and their collectives in the country. A cluster is a group of enterprises
located within an identifiable and as far as practicable, contiguous area or a value chain that
goes beyond a geographical area and producing same/similar products/complementary
products/services, which can be linked together by common physical infrastructure facilities
that help address their common challenges. The essential characteristics of enterprises in a
cluster are <br>(a) Similarity or complementarity in the methods of production, quality control &
testing, energy consumption, pollution control, etc. <br>(b) Similar level of technology &
marketing strategies/practices <br>(c) Similar channels for communication among the members
of the cluster <br>(d) Common market & skill needs and/or (e) Common challenges &
opportunities that the cluster faces. </p>
                       

                      
                      <div style=" padding: 6px; border: 1px dashed #ddd; color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                            <p> 
                       <strong>Objectives of the Scheme: </strong>
                        </p>
                        <p style="text-align:justify">
(i)To support the sustainability and growth of MSEs by addressing common issues such as
improvement of technology, skills & quality, market access, etc.</p>
 <p style="text-align:justify">(ii)	To build capacity of MSEs for common supportive action through formation of self help
groups, consortia, upgradation of associations, etc.</p>
 <p style="text-align:justify">(iii)	To create/upgrade infrastructural facilities in the new/existing Industrial Areas/Clusters of
MSEs. </p>
 <p style="text-align:justify">(iv)	To set up Common Facility Centres (for testing, training, raw material depot, effluent
treatment, complementing production processes, etc).
</p>

                          <p style="text-align:justify">(v)	Promotion of green & sustainable manufacturing technology for the clusters so as to
enable units switch to sustainable and green production processes and products.
</p>


                      </div>
                      <div style=" padding: 6px; border: 1px dashed #ddd; color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                            <p> 
                       <strong>Components:

 </strong>
                        </p>
                        <p style="text-align:justify"> <strong>(i)	Common Facility Centers (CFCs):</strong> The Gol grant will be restricted to 70% of the cost of Project
of maximum Rs.20.00 crore. Gol grant will be 90% for CFCs in NE & Hill States, Island territories,
Aspirational Districts/L WE affected Districts, Clusters with more than 50% (a) micro/ village, (b)
women owned, (c) SC/ST units. The cost of Project includes cost of Land (subject to maximum of
25% of Project Cost), building, pre-operative expenses, preliminary expenses, machinery &
equipment, miscellaneous fixed assets, support infrastructure such as water supply, electricity and
margin money for working capital.
</p> 
 <p style="text-align:justify"> <strong>(ii)	Infrastructure Development:</strong> The Gol grant will be restricted to 60% of the cost of Project
(Rs.lO.OOcrore for Industrial Estate & Rs.15 .00 crore for Flatted Factory Complex). Gol grant wiII
be 80% for Projects in NE & Hilly States, Island territories, Aspirational Districts I LWE affected
Districts, industrial areas / estates / Flatted Factory Complex with more than 50% (a) micro/ village,
(b) women owned, (c) SC/ST units. For existing clusters, upgradation proposals will be based on
actual requirements.
</p>
             


                          <p style="text-align:justify" /> <strong>(iii)	Marketing Hubs / Exhibition Centres by Associations:</strong>The Gol grant will be restricted to 60%
of the cost of Project of maximum Rs.l 0.00 crore for Product Specific Associations with SMO rating
of Gold Category and above from NABET (QCI) and 80% for Associations of Women
Entrepreneurs. Remaining project cost is to be borne by SPV / State Government. The Gol
contribution will be towards construction of building, furnishings, furniture, fitti1gs, items of
permanent display, miscellaneous assets like generators, etc.
<p/>
                              <p style="text-align:justify"> <strong>(iv)	Thematic Interventions: </strong>The Gol grant will be restricted to 50% of total cost of maximum 5
Thematic Interventions not exceeding Rs.2.00 lakh for each in approved / completed CFC for activity
mentioned below. As such the maximum Gol grant under this component for each CFC would be
Rs.IO.OOlakh. Remaining cost would be borne by SPV / State Government.<br />
(a) Training Programmes.<br />
(b) Exposure Visits.<br />
(c) Strengthening the Business Development Service (BDS) provision through a panel of
service providers.<br />
(d) Any other activity related to creating business eco-system in cluster mode.<p /><br />
                                  <p style="text-align:justify"> <strong>(v)	Support to State Innovative Cluster Development Programme:</strong>A few State Governments have
initiated State funded Cluster Development Programme to support soft and hard interventions in
clusters with limited funding support. In order to strengthen this activity, this component would
provide co-funding of the CFC projects of State Cluster development Programme on matching share
basis. The Gol fund would be limited to State Government share or Rs.5.00 crore whichever is
lower. The Gol assistance would be 90% of project cost not exceeding Rs.5.00 crore in respect of
CFC projects in North East/Hilly States, Island territories, Aspirational Districts/L WE affected
Districts, as well as for projects where beneficiaries are SC/ST/Women owned enterprises, as per the
scheme guidelines of State Cluster Development Programme.<p />


