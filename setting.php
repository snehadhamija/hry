	     <h1 id="31">About Skill Development</h1><br><br>
   <p><b>Objectives of the scheme:</b> The objectives of the scheme are as follows:</p> <p><br /></p> <ul><li>To provide sector specific skilled workforce from floor level workers, operators, packaging and assembly line workers to quality control supervisor etc in the various segments of food processing industries.</li> <li>To contribute towards achieving the projected skilled human resources requirement as envisaged by National Skill Development Corporation (NSDC) in food processing sector i.e 17.8 million persons by the year 2022.</li> </ul><p>Apart from implementation of the Skill component under PMKSY, a number of initiatives have been taken by GOI to address  the skill gap in the food processing sector. The Ministry of Food Processing Industries is working in close collaboration with other related agencies to augment skilled manpower in the food processing sector. The Ministry is collaborating with the Food Industry Capacity and Skill Initiative (FICSI), the sector Skill Council (SSC) in food processing, for the validation of the Qualification Packs (QPs) for identified job roles and developing course curriculum for food processing sector through the National Institute of Food Technology Entrepreneurship and Management (NIFTEM). </p> <p><br /></p>                <div style="margin-top:25px;"> <b>Contact Details:</b> <p>Sh. H. K. Pattanaik<br /> Deputy Secretary<br /> Ministry of Food Processing Industries,<br /> Panchsheel Bhawan, August Kranti Marg,<br /> New Delhi-110049<br /> Tel:011-26406530<br /> Email: <a href="mailto:skill-mofpi[at]gov[dot]in" target="_BLANK" title="External site that opens in a new window">skill-mofpi[at]gov[dot]in</a>, <a href="mailto:hkpattan[at]nic[dot]in" target="_BLANK" title="External site that opens in a new window">hkpattan[at]nic[dot]in</a></p></div>
   <br>
   <h1 id="32">Components</h1><br>
   <br>
    <ul><li>Development of Course Curriculum / Training Module in English, Hindi and translation of the same in recognized regional languages based on the QPsvalidated by NSDC as National Occupational Standards.</li> </ul><ul><li>Establishing Training Center (TC) / Expansion of Existing Training Center to impart skill / training on various job roles in food processing.</li></ul>  <br>
	<h1 id="33">Pattern Assistance</h1><br>
	<br>
	<ul><li> Development of Course Curriculum / Training Module in English, Hindi and translation of the same in recognized regional languages based on the Qualification Packs (QP) validated by National Skill Development Corporation (NSDC) as National Occupational Standards: </li><li></li></ul><ul><li> Grants-In Aid would be provided  to the eligible Institutions upto a maximum of Rs. 5.00 lakh per QP for development of training module both in print and Multimedia for each job role and a maximum of Rs. 0.50 lakh per QP would be provided for the translation of already developed training modules in recognized regional Language as per 8th schedule both in print and Multimedia for each job role. Grant  is disbursed on re-imbursement basis after the training modules are developed / translated and further validated by the NSDC / FICSI.  </li> <ul><li> Assistance for creation of infrastructure facilities for Skill Training Centers:</li> <p>Grants-In-Aid is provided at the rate of 50% of cost of plant &amp; machineries required for a National Skill Qualifications Framework (NSQF) validated training module subject to maximum of Rs. 15 lakh per training module and limited to maximum two training modules per TC.</p> </ul></ul>                           
	<br>
