	<h1 id="14"> Pattern Assistance </h1><br>
	<br>
	 <ol type="i"><li><b>For the Government organizations/universities/institutions:</b> <p>Grant-in-aid is given for 100% of cost of equipment, consumables and expenditure related to salaries for project staff specific to the project for maximum period of three years. Grant is released in three instalments.</p> </li> <p><br /></p> <li><b>Funding pattern for Private organizations / universities / institutions:</b> <p>Grant-in-aid is given to the tune of 50% of equipment cost only in general areas and 70% in North East States and difficult areas. Grant is released in three instalments.</p> <p><br /></p> </li><li><b>Ministry’s sponsored projects:<b> </b></b><p>100% Grant is provided only to public funded organizations of repute for cutting edge areas of research leading to development of innovative products, processes, manufacturing practices and technology. </p> </li> </ol><p><br /></p>                
