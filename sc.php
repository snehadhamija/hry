<center><h1 class="heading">National Scheduled Caste and Scheduled Tribe Hub</h1></center>

		<p>India is witnessing rapid growth and prosperity. For this to be truly inclusive, all sections of the society need to partake in this prosperity. Besides wage employment and education, the marginalized sections of the society also need to envision, create and scale - up ventures to be a major participant in this growth story. While in the past, there has been some increase in the number of SC/ST owned enterprises, this needs to increase substantially to enable the socio-economic empowerment of the SC/ST communities. In this context, the National SC/ST Hub is an initiative of the Government of India targeted towards developing a supportive ecosystem towards SC/ST entrepreneurs.</p>
            <p>The National SC/ST Hub (NSSH) would provide professional support to the SC/ST enterprises thereby enabling them to effectively participate in public procurement process. This would involve participation by CPSEs/Central Ministries, States, Industry Associations such as DICCI and others. The Hub would also work towards the development of new entrepreneurs to participate in procurement process leveraging on the ‘Stand up India’ programme. Selected entrepreneurs would be provided with support and mentoring by industry experts, CPSEs, and incubators.</p>
            <p>To start with, the Ministry of Micro, Small and Medium Enterprises (M/o MSME), has made an initial allocation of Rs. 490 Crore for the period 2016-2020 for the National SC/ST Hub. Discussions with various stakeholders have enabled the Ministry to create an initial agenda for action on this front. While the agenda for action is flexible and would evolve with further inputs from stakeholders and more robust data, initially it attempts to address gaps that exist across multiple drivers of the entrepreneurial eco-system.</p>
            <p>The Hub would operate out of the National Small Industries Corporation (NSIC) headquartered in Delhi, supported by a special cell created for this purpose. All new and existing SC/ST entrepreneurs would be registered on the MSME Databank (<a href="http://www.msmedatabank.in" target="_blank">www.msmedatabank.in</a>) of the Ministry of MSME that would form a reliable database to enhance accessibility for the procurement agencies. A separate website <a href="http://www.scsthub.in" target="_blank">www.scsthub.in</a> has been launched to apprise the stakeholders about the activities of the Hub.  </p>
            <p class="text-danger"><b>High Powered Monitoring Committee</b></p>
            <p>Minister of MSME is the chairperson of the committee. The committee members are representatives of various stakeholders including states, various ministries, industry associations & SC/ST entrepreneurs.</p>
            <p class="text-danger"><b>Advisory Committee</b></p>
            <p>The Chairperson of this committee is Shri Milind Kamble, Chairman, DICCI (Dalit Indian Chamber of Commerce & Industry). This committee would  work under the High Powered Monitoring Committee in order to   bring the industry and SC/ST entrepreneurs’ perspective to the Hub with the purpose of developing an effective, mutually beneficial relationship between the target group and the Government.</p>
            <p class="text-danger"><b>Empowered Project Approval Committee</b></p>
            <p>Secretary, Ministry of MSME chairs this committee. As the Hub is dynamic in nature and would evolve over the envisaged period, requirement of funds for various activities is likely to evolve depending on the emphasis areas or changes in identified focus areas. This Committee would ensure such flexibilities with a view to accommodate changing requirements of SC/ST entrepreneurs.</p>
