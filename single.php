            <p class="text-danger"><b>Benefits of Registration</b></p>
            <p>The units registered under Single Point Registration Scheme of NSIC are eligible to get the benefits under Public Procurement Policy for Micro & Small Enterprises (MSEs) Order 2012 as notified by the Government of India, Ministry of Micro Small & Medium Enterprises, New Delhi vide Gazette Notification dated 23.03.2012 and amendment vide order no. S.O. 5670(E) dated 9th November 2018.</p>
            <ul>
                <li>Issue of the Tender Sets free of cost;</li>
                <li>Exemption from payment of Earnest Money Deposit (EMD);</li>
                <li>In tender participating MSEs quoting price within price band of L1+15 per cent shall also be allowed to supply a portion upto 25% of requirement by bringing down their price to L1 Price where L1 is non MSEs.</li>
                <li>Every Central Ministries/Departments/PSUs shall set an annual goal of minimum 25 per cent of the total annual purchases of the products or services produced or rendered by MSEs. Out of annual requirement of 25% procurement from MSEs, 4% is earmarked for units owned by Schedule Caste /Schedule Tribes and 3% is earmarked for the units owned by Women entrepreneurs.</li>
                <li>In addition to the above, 358 items are also reserved for exclusive purchase from SSI Sector (List is given below in download section).</li>
            </ul>
            <p class="text-danger"><b>Eligibility</b></p>
            <ul>
                <li>All Micro & Small Enterprises having EM Part-II (Optional)/ Udyog Aadhaar Memorandum (UAM) are eligible for registration with NSIC under its Single Point Registration Scheme (SPRS).</li>
                <li>Micro & Small Enterprises who have already commenced their commercial production but not completed one year of existence. The Provisional Registration Certificate can be issued to such Micro & Small Enterprises under Single Point Registration scheme with monitory limit of Rs. 5.00 Lacs which shall be valid for the period of one year only from the date of issue after levying the registration fee and obtaining the requisite documents.</li>
            </ul>
            <p class="text-danger"><b>How to Apply </b></p>
            <p>Micro & Small Enterprises (MSEs) shall have to apply either online on our website www.nsicspronline.com or on the prescribed application form in Duplicate and to be submitted to the concerned Zonal/Branch Office of NSIC located nearest to the unit. In case of any difficulty in filling the application form and completing the documentation, please consult any of the Zonal / Branch office of NSIC. The application form containing Terms & conditions are available free of cost from all offices of the NSIC. The guidelines attached with the Application Form provide a checklist for the documents that are required to be submitted along with the application.</p>
            <p class="text-danger"><b>REGISTRATION FEE</b></p>
            <p>The registration Fee is based on the Net Sales Turnover as per latest audited Balance Sheet of the Micro & Small Enterprise for the Registration, Renewal and any other amendment etc. For detailed fee structure please see the download section below.</p>
<hr color="black" size="2">
            <p>
               <b><u><font color="balck">The Registration Fee is excluding the Inspection charges. For details of inspecting agencies and their fee please see the download section below.</u></font></b>
            </p>
            
<hr/>
            <p class="text-danger"><b>REGISTRATION PROCESS</b></p>
            <ol>
                <li>Micro & Small Enterprises shall have to apply either online on our website www.nsicspronline.com or on the prescribed application form (in duplicate) along-with requisite fee and documents to the Zonal/Branch/Sub Branch and Sub Office/Extension office of NSIC situated nearest to their location.</li>
                <li>Duplicate copy of the G.P. Registration Application Form submitted by the Micro & Small Enterprise will be forwarded to the concerned Inspecting agency along with copies of required documents and requisite Proof of inspection charges remitted in favor of concerned Inspection Agency requesting for carrying out the Technical Inspection of Micro & Small Enterprise and forward their recommendations in this regard.</li>
                <li>After receiving Inspection Report, NSIC will issue the SPRS Registration Certificate to Micro & Small Enterprise for items/stores as recommended.</li>
            </ol>
            <p class="text-danger"><B>Procedure for calculation & fixation of Monetary Limits of Micro & Small Enterprises.</B></p>
            <p>Monetary limit of the company is fixed on the basis of the unit's net sales turnover during the last three years reflected in the Audited Balance Sheets.</p>
            <p>
                Monetary limit will be fixed on the basis of highest turnover during the last three year which may or may not be of last year provided the units installed and operating capacity has not been reduced.
            </p>
            <ul>
                <li>In case there is no decrease in plant and machinery, than 50% of highest turnover during the last 3 years reflected in audited balance sheet will be the basis for fixation of monetary limit.</li>
                <li>In case there is decrease in plant and machinery for more than 10%, the following will be considered:                    <ol>
                        <li>Where the turnover of the Enterprise has steadily increased over the last three years and the unit is in profit continuously, the Monetary Limit may be fixed at 50% of net sales turnover achieved in the last year</li>
                        <li>In case the Company/Partnership concern/Proprietorship unit is in loss for one year out of past three years, their monetary limit will be fixed at 40% of their average net sales turnover.</li>
                        <li>Similarly, when the Micro & Small Enterprise is in loss for two years out of the past three years, the monetary limit will be accordingly fixed at 30% of their average net sales turnover of the past three years.</li>
                        <li>In the event of Micro & Small Enterprise being in loss throughout past three years, the monetary limit of the Unit will be fixed at 20% of the average net sales turnover of the Unit during the past three years.</li>
                    </ol>
                </li>
            </ul>
            <p class="text-danger"><b>Validity Period of G. P. Registration</b></p>
            <p>The Registration Certificate granted to the Micro & Small Enterprise under Single Point Registration Scheme (Revised), 2003 is valid for Two Years and will be reviewed and renewed after every two years by verifying continuous Commercial and Technical Competence of the registered Micro & Small Enterprise in manufacturing / producing the stores for which it has been registered by NSIC.</p>
