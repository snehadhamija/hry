<p><strong>Process of Registration under 35 AC</strong></p>

<ul class="abc" class="abc">
	<li>&nbsp;Section 35AC (Statutory Provisions)</li>
</ul>

<p>As part of our &lsquo;Online Services&rsquo; we provide the services to register the Non Governmental and Non Profitable organization under section 35ac of Income Tax Act in India.</p>

<p>Under section 35AC, organizations having income from business or profession can get 100 per cent deduction. Charitable Organizations can get registered themselves if they are carrying on any business.</p>

<p>The Central Government has specified various types of projects of national needs for which Charitable Organizations can take donations.</p>

<p>The approval under section 35(AC) is not permanent in nature.</p>

<p>The National Committee may recommend or reject the project but when the approval is recommended then it is for a period of maximum 3 years and it could be further extended if the National Committee is satisfied with the performance during the period.</p>

<p>The National Committee may withdraw the approval if the project is not carried out in accordance with the approved conditions. To withdraw a project National Committee should provide an opportunity of being heard to the aggrieved organization.</p>

<p><strong>Requirements</strong></p>

<p>Duly registered organization.</p>

<p>Detailed Project for Charitable purposes*.</p>

<p><strong>Documents and information we need</strong></p>

<p>&nbsp;Name, address and status of applicant, the district/ ward circle where assessed/PAN number.</p>

<ol>
	<li>Audited Balance Sheet, Profit&amp; Loss Account or Income&amp; Expenditure Account for the latest year and two preceding years.</li>
	<li>&nbsp;Formation documents like trust deed, rules &amp; regulation, memorandum of association etc. and registration certificate, if any.</li>
	<li>Name &amp; Addresses of the persons managing the affairs of the association or institution, including those who left the organization but were managing the affairs of the association or institution during the 3 years preceding the date of application.</li>
	<li>If the association or institution is notified under section 10(23)(C) or is approved for the purposes of section 80G, the particulars of such approval granted.</li>
	<li>Brief particulars of the activities of the association or institution during 3 years preceding the date of application or since inception if the association or institution is less than 3 years old.</li>
	<li>Such other information as the association or institution may like to place before the National Committee.</li>
	<li>Signed Authority Letter</li>
</ol>

