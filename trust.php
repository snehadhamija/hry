<p>The application for approval of an association or institution should contain the following particulars and documents:</p>

<ol>
	<li>Name, address and status of applicant, the district/ward/circle where assessed, Permanent Account Number, audited balance sheet and profit and loss account or income and expenditure account for three years including the latest year;</li>
	<li>Name and address of the association or institution along with trust deed, rules and regulations, memorandum of association etc. and registration certificate, if any;</li>
	<li>Names and addresses of the persons managing the affairs of the association or institution, including the names of such persons for last three years;</li>
	<li>If notified, u/s 10(23C) (iv) or (v) of the Income Tax Act, 1961 or approved under section 80G, the particulars thereof;</li>
	<li>Brief particulars of the activities of the association or institution during the last three years.</li>
</ol>
