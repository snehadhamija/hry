<p><strong>What is an eligible project or scheme </strong></p>

<p>An eligible project or scheme is one, which the Central Government on the recommendation of the National Committee, may so notify in the Official Gazette. It should relate to one or more of the following:-</p>

<ul class="abc" class="abc">
	<li>construction and maintenance of drinking water projects in rural areas and in urban slums, including installation of pump-sets, digging of wells, tube wells and laying of pipes for supply of drinking water;</li>
	<li>construction of dwelling units for the economically weaker sections;</li>
	<li>construction of school buildings primarily for children belonging to the economically weaker sections of the society;</li>
	<li>establishment and running of non-conventional and renewable sources of energy systems;</li>
	<li>construction and maintenance of bridges, public highways and other roads;</li>
	<li>Promotion of sports;</li>
	<li>Pollution control;</li>
	<li>Any other programme for uplift of the rural poor or the urban slum dwellers, as the National Committee may consider fit for support, like:-</li>
</ul>

<ol>
	<li>Family welfare and immunization.</li>
	<li>Tree plantation</li>
	<li>Social forestry</li>
	<li>Development of irrigation resources</li>
	<li>Rural sanitation-construction of low &ndash;cost latrines</li>
	<li>Medical camps in rural areas</li>
	<li>Rural health programmes</li>
	<li>Land development and reclamation of waste land or degraded land with special emphasis on ecological improvement</li>
	<li>Soil and water conservation including harvesting of run off water</li>
	<li>Non-formal education and literacy, especially for women &amp; children</li>
	<li>Rural non-farm activities</li>
	<li>Creation of employment opportunities for urban and rural population living below the poverty line</li>
	<li>Supportive services for women to engage in productive work (care of children of working women by providing an improved environment, care and food and by establishing cr&egrave;ches/balwadis, etc.)</li>
	<li>Leprosy eradication</li>
</ol>

<p><strong>Functions of the National Committee</strong></p>

<p>The Central Government has constituted this Committee with the following functions:</p>

<ul class="abc" class="abc">
	<li>To approve associations and institutions for the purpose of carrying out any eligible project or scheme; and</li>
	<li>To recommend to the Central Government projects and scheme of any company including a public sector company, a local authority or an approved association or institution, for being notified as eligible projects or scheme.</li>
</ul>

