<p><strong>Introduction</strong></p>

<p>In order to promote reinvestment of business profits in areas where massive capital input is required for socio-economic development, a tax incentive has been provided under Section 35AC of the Income Tax Act, 1961 allowing for full deduction of the entire amount paid by a taxpayer carrying on a business or profession for financing projects or schemes promoting social and economic welfare. In the case of other taxpayers, the deduction is allowed under section 80GGA from his gross total income. Who can undertake eligible projects and schemes</p>

<ul class="abc" class="abc">
	<li>An association</li>
	<li>An institution</li>
	<li>A public sector company</li>
	<li>A local authority</li>
	<li>A company, which is a taxpayer may avail of this deduction either by way of payment to the above mentioned entities or directly by undertaking an eligible project or scheme.</li>
	<li>Conditions necessary for approval</li>
	<li>the association or institution is;
	<ul class="abc" class="abc">
		<li>Constituted as a public charitable trust. Registered under the Societies Registration Act, 1860 (Act 21 or 1860) or under any law corresponding to that Act.</li>
		<li>Registered under section 25 of the Companies Act, 1956 (Act 1 of 1956).</li>
	</ul>
	</li>
</ul>

<ol>
	<li>Persons managing the affairs of the association or institution are persons of proven integrity;</li>
	<li>The activities of the association or institution are open to citizens of India without any distinction and are not for the benefit of any individual or community;</li>
	<li>The association or institution maintains regular accounts of its receipts and expenditure;</li>
	<li>The instrument or the rules or regulations governing the association or institution do not contain any provision for the transfer or application at any time, of the whole or any part of its income or assets for any purpose other than a charitable purpose.</li>
</ol>

