              <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Introduction</strong></p>
 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. Of all the problems faced  by the MSEs, non-availability of timely and adequate credit at reasonable  interest rate is one of the most important. One of the major causes for low  availability of bank finance to this sector is the high risk perception of the  banks in lending to MSEs and consequent insistence on collaterals which are not  easily available with these enterprises. The problem is more serious for micro  enterprises requiring small loans and the first generation entrepreneurs.</p>
<p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
2. The Credit Guarantee Fund  Scheme for Micro and Small Enterprises (CGS) was launched by the Government of  India (GoI) to make available collateral-free credit to the micro and small  enterprise sector. Both the existing and the new enterprises are eligible to be  covered under the scheme. The Ministry of Micro, Small and Medium Enterprises,  GoI and Small Industries Development Bank of India (SIDBI), established a Trust  named Credit Guarantee Fund Trust for Micro and Small Enterprises (CGTMSE) to  implement the Credit Guarantee Fund Scheme for Micro and Small Enterprises. The  scheme was formally launched on August 30, 2000. The corpus of CGTMSE is being  contributed by the GoI and SIDBI in the ratio of 4:1 respectively and has  contributed Rs. 2477.78 crore to the corpus of the Trust up to May 31, 2016. As  announced in the Package for MSEs, the corpus was to be raised to Rs.2500 crore  by the end of 11th Plan.</p>
  

                                   
                      </div>
                     
                     <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Eligible Lending Institutions</strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. The Banks / Financial  Institutions, which are eligible under the scheme, are scheduled commercial  banks (Public Sector Banks/Private Sector Banks/Foreign Banks) and select  Regional Rural Banks (which have been classified under 'Sustainable Viable' category  by NABARD). As on May 31, 2016, there were 133 eligible Lending Institutions  registered as MLIs of the Trust, comprising of 26 Public Sector Banks, 21  Private Sector Banks, 73 Regional Rural Banks (RRBs), 4 Foreign Banks and 9  other institutions i.e. Delhi Financial Corporation, Kerala Financial  Corporation, Jammu &amp; Kashmir Development Finance Corporation Ltd, Andhra  Pradesh State Financial Corporation, Export Import Bank of India, The Tamil  Nadu Industrial Investment Corporation Ltd., National Small Industries Corporation  (NSIC), North Eastern Development Finance Corporation (NEDFI) and Small  Industries Development Bank of India (SIDBI).</p>
                     </div> 
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Eligible Credit Facility</strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. The credit  facilities which are eligible to be covered under the scheme are both term  loans and/or working capital facility up to Rs.100 lakh per borrowing unit,  extended without any collateral security and / or third party guarantee, to a new  or existing micro and small enterprise. For those units covered under the  guaranteescheme, which may become sick owing to factors beyond the control of  management, rehabilitation assistance extended by the lender could also be  covered under the guarantee scheme. Any credit facility in respect of which  risks are additionally covered under a scheme, operated by Government or other  agencies, will not be eligible for coverage under the scheme.</p>
                      </div>
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Guarantee Cover </strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. The guarantee cover  available under the scheme is to the extent of maximum 85% of the sanctioned  amount of the credit facility. The guarantee cover provided is up to 75% of the  credit facility up to Rs.50 lakh (85% for loans up to Rs. 5 lakh provided to  micro enterprises, 80% for MSEs owned/ operated by women and all loans to NER  including Sikkim) with a uniform guarantee at 50% for the entire amount if the  credit exposure is above Rs.50 lakh and up to Rs.100 lakh. In case of default,  Trust settles the claim up to 75% (or 85% / 80% / 50% wherever applicable) of the  amount in default of the credit facility extended by the lending institution.  For this purpose the amount in default is reckoned as the principal amount  outstanding in the account of the borrower, in respect of term loan, and amount  of outstanding working capital facilities, including interest, as on the date  of the account turning Non-Performing Asset (NPA).</p>
 

                                   
                      </div>
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Tenure of Guarantee </strong></p>
 
 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6. The Guarantee cover under  the scheme is for the agreed tenure of the term loan/composite credit. In case  of working capital, the guarantee cover is of 5 years or block of 5 years.</p>
 

                                   
                      </div>
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Fee for Guarantee</strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7. A composite all-in Annual  Guarantee Fee of 1.0 % p.a. of the credit facility sanctioned (0.75% for credit  facility upto Rs. 5 lakh and 0.85% for above Rs. 5 lakh and upto 100 lakh for  Women, Micro Enterprises and units in NER including Sikkim) is now being  charged.</p>
  

                                   
                      </div>
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Website / Circulars</strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8. Details of the scheme along with latest circulars  are available in the website of CGTMSE at <a href="http://www.cgtmse.in">www.cgtmse.in</a>. </p>  

                                   
                      </div>
                      <div style=" padding: 6px;  color: #333; margin-bottom: 20px; padding: 10px 30px 0px 30px;">
                <p style="text-align:justify"><strong>Scheme Awareness Programmes</strong></p>
 

 <p style="text-align:justify; padding-left:20px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9.1 CGTMSE has adopted multi-channel approach for creating awareness of the CGS amongst banks, MSE industry associations, MSE sector, etc. through print and press media, conducting workshops / seminars, attending the programmes organized at various district / state / national fora, etc. During the year, CGTMSE participated in various seminars / workshops organized by MLIs andIndustry Associations, exhibitions and meetings organized by RBI / Govt. in connection with MSE sector, across the country to create awareness about CGS. CGTMSE officials also held business development meetings with its Member Lending Institutions. Sustained print media campaigns were carried out across the country throughout the year to improve visibility and create awareness about the scheme. Information dissemination campaigns were vigorously carried out amongst various stakeholders.
                </p>  

      <p style="text-align:justify; padding-left:20px;">
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9.2 During FY 2016, CGTMSE participated in 21 Seminars/ Workshops/ Bankersâ€™ meet/ Business Development Meetings, and also made presentations to sensitize bank officials/ small enterprises on the various aspects of the Credit Guarantee Scheme. The workshops/ programmes were generally arranged by the member banks / SIDBI / CGTMSE / Industry Associations, etc.
      
      </p>                             
