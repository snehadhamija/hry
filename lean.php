                  <font color="#660000" size="+1" face="Verdana, Arial, Helvetica, sans-serif">Lean Manufacturing Competitiveness Scheme (LMCS) for Micro Small and Medium Enterprises(MSMEs)</font></span>

					<table width="90%" align="center">
                        <tr><td colspan="2">
					
                        <div align="justify">
                          <br>
                              <strong>The Scheme :-</strong> <br>
                              <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Under the Scheme, MSMEs will be assisted in reducing their manufacturing costs, through proper personnel management, better space utilization, scientific inventory management, improved processed flows, reduced engineering time and so on. LMCS also brings improvement in the quality of products and lowers costs, which are essential for competing in national and international markets.  The larger enterprises in India have been adopting LMCS to remain competitive, but MSMEs have generally stayed away from such Programmes as they are not fully aware of the benefits. Besides, experienced and effective Lean Manufacturing Counsellors or Consultants are not easily available and are expensive to engage and hence most MSMEs are unable to afford LMCS.<br><br>
							  
							  Scheme has been approved as a pilot project in 100 Mini Clusters for implementations of Lean Techniques.<br><br>
                              <br>
                            </div>
						  
                           
                            <div align="justify"><strong>Objectives:-</strong> 
                              <br>
                              <br>
                              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The main objective 
                              of the Lean Manufacturing Competitiveness Scheme 
                              (LMCS) is to bring the manufacturing competitiveness 
                              in the MSME Sector. Lean Manufacturing involves 
                              applying Lean Techniques (e.g. Total Productive 
                              Maintenance (TPM), 5S, Visual control, Standard 
                              Operation Procedures, Just in Time, Kanban System, 
                              Cellular Layout, Poka Yoke, TPM, etc.) to identify 
                              and eliminate waste and streamline a system. The 
                              focus is on making the entire process flow, not 
                              improving only a few operations. Worker empowerment 
                              is also emphasized throughout the effort. </div>
                            <div align="justify"><br>
                              <br>
                              <strong>Approach:- </strong> <br>
                              <br>
                              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The approach involves 
                              engagement of Lean Manufacturing Consultants (LMCs) 
                              to assess the existing manufacturing system of member 
                              units of the Mini Cluster(s) and stipulate detailed 
                              step by step procedures and schedules for implementing 
                              and achieving of lean techniques. <br>
                              <br>
                              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A Special Purpose 
                              Vehicle (SPV) will be formed in each cluster. It 
                              is expected that once MSMEs are introduced to the 
                              benefits and savings that accrue from LM techniques, 
                              they would themselves continue the Scheme from the 
                              second year onwards at their own expense. A three 
                              tier implementing structure is in place with a group 
                              of 10 or so MSMEs (SPV) at the lowest local-tier 
                              and a Lean Manufacturing Screening and Steering 
                              Committee (SSC) under DC(MSME) at the highest tier<br>
                              <br>
                              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The intermediate tier, 
                              National Monitoring and Implementing Unit (NMIU) 
                              is responsible for facilitating implementation and 
                              monitoring of the Scheme. For the Pilot phase of 
                              100 Mini Clusters, it is envisaged that NPC (National 
                              Productivity Council) function as NMIU. </div>
                            <div align="justify"><br>
                              <br>
                              <strong>Assistance:-</strong> <br>
                              <br>
                              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A financial support 
                              by the Government of India upto a maximum of 80% 
                              of the Consultant fees for each Mini Cluster will 
                              be provided. Remaining 20% is to be brone by the 
                              beneficiaries MSME units. </div>
                            <div align="justify"><br>
                              <br>
