			<h1>Trade Related Entrepreneurship Assistance and Development (TREAD) Scheme for Women</h1>
<TABLE ALIGN=CENTER WIDTH=95% CELLSPACING=0 cellpadding=4>


<tR><TD><p> Traditionally, women have been amongst the most disadvantaged sections of our society with regard to access to and control over resources. Problems faced by them continue to be grave particularly for illiterate and semi-literate women of rural and urban areas. Apart from counseling and training, availability of credit poses the most serious problem for the poor women. In order to alleviate their problems, the Ministry of Small Scale Industries (SSI) in the Government of India has launched a scheme titled "Trade Related Entrepreneurship Assistance and Development" (TREAD) for economic empowerment of women through the development of their entrepreneurial skills in non-farm activities. The Office of the Development Commissioner (SSI) in the Ministry of SSI is responsible for the implementation of this scheme.

</td></tr>
<tr>
<td><p>
2. The three main components of this scheme are as under: 
  <p>(i) Assistance in the form of the Government of India (GoI) grant of up to 30 per cent of the total project cost, as appraised by the lending agency is provided to the Non Governmental Organisations (NGOs) for promoting entrepreneurship among target groups of women. The remaining 70 per cent of the project cost is financed by the lending agency as loan for undertaking activities as envisaged in the project. The NGOs can utilise the grant for training, counselling, tie ups for marketing on behalf of the beneficiaries, etc. besides their own capacity building for assisting women. </p>
  <p>(ii) Selected training institutions and NGOs conducting training programmes for empowerment of women beneficiaries identified under this scheme can also avail of GoI grant of up to Rs.1.00 lakh per programme, for imparting training to the women entrepreneurs, provided such institutions also bring their share of at least 25 per cent of the grant. However, NGOs except from the North East Region (NER), would be allowed to conduct training programmes only after they take up some lending proposals under this scheme. </p>
  <p>(iii) Need-based GoI grant upto Rs.5 lakh per project is provided to National Entrepreneurship Development Institutions (EDIs) viz. National Institute of Small Industry Extension Training (NISIET), National Institute of Entrepreneurship and Small Business Development (NIESBUD), Indian Institute of Entrepreneurship (IIE), EDIs sponsored by the State Government, Small Industries Service Institutes (SISIs), and any other institution of repute primarily for undertaking field surveys, research studies, evaluation studies, designing of training modules, etc. </p></td></tr>
<tr>
  <td><p>3. In order to implement this scheme, the Ministry of SSI, through the office of Development Commissioner (SSI) has signed Memorandum of Understandings (MoUs) with the Canara Bank, the Syndicate Bank, and the State Bank of India for mutual co-operation and to promote empowerment of women through setting up of micro enterprises under this scheme. It is the endeavour of this office to further expand the coverage of this scheme across the country by involving more banks as nodal agencies for implementing the TREAD scheme. As a part of this objective, this office has also requested some more public sector banks for becoming the nodal agencies for implementing the TREAD scheme. </p>
    </td>
</tr>
<tr>
  <td> 4. &nbsp; There is a Steering Committee on TREAD under the chairmanship of Additional Secretary &amp; Development Commissioner (SSI), which provides an&nbsp;overall guidance for the promotion and implementation of the Scheme.&nbsp; In addition to undertaking regular monitoring and review of the implementation of the projects in consultation with the lending agencies, NGOs, training institutions, and other stake holders, the Steering Committee also approves the GoI grant to be released through the lending agency (ies) during its periodic meetings. Based on the progress made and experiences achieved in the implementation of the scheme, the Steering Committee is also empowered to revise the eligibility criteria for the NGOs, structure / design of the scheme and the format for application to be submitted for availing benefits under the scheme. </td>
</tr>
<tr>
  <td> 5. During the review of the progress of the scheme by Steering Committee on TREAD in its meeting held in July, 2005 it was observed that Micro Finance Institutions (MFI) being financially viable, self- sustaining and integral to the community in which they operate, have the potential to attract more resources and expand services to clients.&nbsp;Hence, the Steering Committee decided that MFI will also be allowed to raise loan on behalf of women beneficiaries under the TREAD Scheme.&nbsp; </td>
</tr>
<tr>
  <td><p>6.&nbsp;&nbsp;&nbsp;&nbsp; <strong>Permission to NGOs based in North East Regions (NER) for conducting training programmes under the TREAD scheme. </strong></p>
    <p>In November 2005, a meeting was organised at IIE, Guwahati to review the implementation of the TREAD scheme in the North Eastern Regions (NER). Most of the NGOs present during this meeting were of the view that women entrepreneurs based in the NER require adequate training before they could be persuaded to take up entrepreneurial activity by availing institutional loans and other assistance under the scheme. Hence, there was a general demand that NGOs from the NER should be allowed to conduct training programmes under this scheme, irrespective of whether they have taken up some lending proposals for the women beneficiaries, as required under the scheme. </p>
    <p>The Steering Committee on TREAD in its meeting held on February 14, 2006, considered this request and decided that only proposals of NGOs from the NER routed through IIE, Guwahati and National Small Industries Corporation Limited (NSIC), Guwahati would be considered for allowing them to conduct training programmes under this scheme. </p></td>
</tr>
<tr>
  <td><p><strong>7. Eligibility requirement of an NGO under the TREAD scheme: </strong></p>
    <p>(a)&nbsp; Be a legal entity with a minimum registration of three years. </p>
    <p>(b)&nbsp; Having experience in thrift and saving programmes with Self-Help Groups (SHGs). </p>
    <p>(c)&nbsp; Be engaged in income generation activities for women entrepreneurs. </p>
    <p>(d)&nbsp; Have basic infrastructure, qualified support staff and services to undertake micro-enterprise development for women. </p>
    (e)&nbsp; Prior experience in preparing project proposals on behalf of women entrepreneurs / women SHGs for income generation activities and take loan from financial institutions for onward disbursement to them. </td>
</tr>
<tr>
  <td><p><strong>8. Grant assistance for Capacity Building of NGOs : </strong></p>
    <p>(a)&nbsp; Training expenses of operating staff on reducing scale. </p>
    <p>(b)&nbsp; Part of the expenses on operationalsing a management and monitoring system, i.e. computer hardware and software for MIS and vehicles. </p>
    <p>(c)&nbsp; Charges for legal documentation, i.e. for payment of stamp duty for loan agreements, legal expenses, valuation of assets, scrutiny and vetting of title deeds, etc. </p>
    <p>(d)&nbsp; Auditor's fees for auditing credit programme records. </p>
    <p>(e)&nbsp; For establishment of new SHGs. </p>
    <p>(f)&nbsp; Common training / consultancy / networking inputs for the members (borrowers) relating to marketing, designing, packaging, quality control, technologies, etc. </p></td>
</tr>
<tr>
  <td><p><strong>9. Grant assistance to be used by NGOs for ultimate borrower: </strong></p>
    <p>(a)&nbsp; Training in credit uses, credit management and basic accounting practices. </p>
    <p>(b)&nbsp; Skill training and professional consultancy inputs, i.e. marketing, designing, packaging, quality control, technology transfer, financial training, etc. </p>
    <p>(c)&nbsp; Equity / margin money contribution for acquisition / establishment of infrastructure (including machine and equipment) in group, entrepreneurship projects. </p>
    <p>(d)&nbsp; Payment of insurance premium on productive assets acquired by beneficiaries. </p>
    <p>(e)&nbsp; Expenses trips to successful micro-credit programmes. </p>
    <p>(f)&nbsp; Legal documentation charges for loan documentation, if any. </p>
    <p>(g)&nbsp; Legal consultancy and training expenses for building institutions of the borrowers, i.e. SHGs, Federation, or Credit Cooperative, etc. </p>
    <p>(h)&nbsp; Participation in exhibition and other marketing events. </p></td>
</tr>
<tr>
  <td><p>10.&nbsp; <strong>Procedure for submitting application for availing GoI : </strong></p>
    Applications (in duplicate) in the prescribed format containing the details of the projects and the profiles of the applicant beneficiaries may be submitted to the Office of DC(SSI), Nirman Bhawan, New Delhi - 110011. Application will be subject to preliminary scrutiny for determining the basic eligibility level. Proposals found to be in order will be forwarded to the lending agencies for detailed evaluation and to make recommendations for approval of the total project cost, loan component and the GoI grant component. However, the GoI grant component will be subject to the final approval of the Steering Committee on TREAD. The progress of the project shall be monitored by the lending agency and it shall be mandatory for all the NGOs and other organizations/ agencies receiving grant under this scheme to submit the Utilisation Certificate in respect of the GoI grant. </td>
</tr>
